                    <p>
                    Bursa Botanik Parkı, İzmir-İstanbul yolu üzerinde Bursa Hayvanat Bahçesi'nin bitişiğinde kurulmuş 400 dönümlük parktır.

1998'den beri 1. derecede doğal sit alanıdır. Bitkisel araştırma ve bilimsel çalışmalara açık bir parktır. Parkın yapımı 1995'te başlamış ve 1998 yılında kullanıma açılmıştır.

Park içinde Spor için koşu yolları, yürüyüş yolları, bisiklet yolları, kültür fizik aletleri, masa tenisi alanları ve bir otomobil pisti bulunur. Parkta bisiklet kiralamak mümkündür. 150 tür ağaç, 27 çeşit gül, 76 tür çalı, 20 tür örtücü bitki bulunur.

Botanik Park'ta Japon Bahçesi, Fransız Bahçesi, İngiliz Bahçesi, gül bahçesi, kaya bahçesi, kokulu bitkiler bahçesi, şekilli bitkiler bahçesi gibi adlarla değişik bahçe alanları oluşturulmuştur. Her yıl uluslararası Lale Festivali nedeniyle 200-250.000 lale ekilir.

Parkın projesini İTÜ Mimarlık Fakültesi öğretim üyelerinden Ahmet C. Yıldızcı hazırlamıştır. Bursa Evleri projesini ise Y. Mimar Hüsrev Tayla gerçekleştirmiştir.

